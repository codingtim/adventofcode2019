    private fun split(s: String): MutableList<Int> {
        return s.split(",").map { Integer.parseInt(it) }.toMutableList()
    }

    private fun opcode(input: MutableList<Int>): String {
        var index = 0
        while (true) {
            index = executeValueOf(input, index)
            if (input[index] == 99) break
        }
        return input.joinToString(separator = ",")
    }

    private fun executeValueOf(split: MutableList<Int>, index: Int): Int {
        fun valueOfParameter(paramMode: Int, offset: Int): Int {
            return if(paramMode == 0) split[offset] else offset
        }
        val value = split[index]
        val operation = value % 10
        val param1Mode = value / 100 % 10
        val param2Mode = value / 1000 % 100
        val param3Mode = value / 10000 % 1000
        if (operation == 1) {
            split[valueOfParameter(param3Mode, index + 3)] = split[valueOfParameter(param1Mode, index + 1)] + split[valueOfParameter(param2Mode, index + 2)]
            return index + 4
        }
        if (operation == 2) {
            split[valueOfParameter(param3Mode, index + 3)] = split[valueOfParameter(param1Mode, index + 1)] * split[valueOfParameter(param2Mode, index + 2)]
            return index + 4
        }
        if (operation == 3) {
            split[valueOfParameter(param1Mode, index + 1)] = 1
            return index + 2
        }
        if (operation == 4) {
            println(split[valueOfParameter(param1Mode, index + 1)])
            return index + 2
        }
        throw IllegalStateException("Unknown operation $operation")
    }
